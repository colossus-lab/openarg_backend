from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class DataResult:
    """Normalized result from any data connector."""

    source: str
    portal_name: str
    portal_url: str
    dataset_title: str
    format: str  # json, csv, time_series, geo
    records: list[dict]
    metadata: dict = field(default_factory=dict)
    # metadata keys: total_records, fetched_at, description, units, last_updated


@dataclass
class PlanStep:
    """A single step in an execution plan."""

    id: str
    action: str  # search_ckan, query_series, query_georef, query_ddjj, query_argentina_datos, query_sesiones, query_staff, query_bcra, query_sandbox, analyze, compare
    description: str
    params: dict = field(default_factory=dict)
    depends_on: list[str] = field(default_factory=list)


@dataclass
class ExecutionPlan:
    """Structured plan generated by the planner from a user query."""

    query: str
    intent: str
    steps: list[PlanStep] = field(default_factory=list)
    suggested_visualizations: list[str] = field(default_factory=list)


@dataclass
class ChartData:
    """Chart data for frontend visualization."""

    type: str  # line_chart, bar_chart, pie_chart
    title: str
    data: list[dict]
    x_key: str
    y_keys: list[str]


@dataclass
class MemoryContext:
    """Conversational memory maintained across turns."""

    turn_number: int = 0
    summaries: list[str] = field(default_factory=list)
    key_findings: list[str] = field(default_factory=list)
    datasets_used: list[str] = field(default_factory=list)
    pending_questions: list[str] = field(default_factory=list)
